pub struct Geometry3D {
	pub indices: Vec<u16>,
	pub attributes: Vec<f32>
}

impl Geometry3D {
	pub fn create_triangle() -> Self {
		let indices = vec![
			0, 1, 2
		];

		let attributes = vec![
			0.0, -0.5, 0.0, 0.0, 0.0, -1.0,
			0.5,  0.5, 0.0, 0.0, 0.0, -1.0,
		   -0.5,  0.5, 0.0, 0.0, 0.0, -1.0,
		];

		Self {
			indices,
			attributes
		}
	}

	pub fn create_plane() -> Self {
		let indices = vec![
			0, 2, 3,
			0, 1, 2
		];

		let attributes = vec![
		   -0.5, -0.5, 0.0, 0.0, 0.0, -1.0,
			0.5, -0.5, 0.0, 0.0, 0.0, -1.0,
			0.5,  0.5, 0.0, 0.0, 0.0, -1.0,
		   -0.5,  0.5, 0.0, 0.0, 0.0, -1.0
		];

		Self {
			indices,
			attributes
		}
	}

	pub fn create_box() -> Self {
		let indices = vec![
			0,  1,  2,  // top
			0,  2,  3,
			4,  6,  5,  // bottom
			4,  7,  6,
			8,  9,  10, // right
			8,  10, 11,
			12, 14, 13, // left
			12, 15, 14,
			16, 18, 17, // front
			16, 19, 18,
			20, 21, 22, // back
			20, 22, 23
		];

		let attributes = vec![
		   -0.5, -0.5,  0.5,  0.0, -1.0,  0.0, // top
			0.5, -0.5,  0.5,  0.0, -1.0,  0.0,
			0.5, -0.5, -0.5,  0.0, -1.0,  0.0,
		   -0.5, -0.5, -0.5,  0.0, -1.0,  0.0,
		   -0.5,  0.5,  0.5,  0.0,  1.0,  0.0, // bottom
			0.5,  0.5,  0.5,  0.0,  1.0,  0.0,
			0.5,  0.5, -0.5,  0.0,  1.0,  0.0,
		   -0.5,  0.5, -0.5,  0.0,  1.0,  0.0,
			0.5, -0.5,  0.5,  1.0,  0.0,  0.0, // right
			0.5,  0.5,  0.5,  1.0,  0.0,  0.0,
			0.5,  0.5, -0.5,  1.0,  0.0,  0.0,
			0.5, -0.5, -0.5,  1.0,  0.0,  0.0,
		   -0.5, -0.5,  0.5, -1.0,  0.0,  0.0, // left
		   -0.5,  0.5,  0.5, -1.0,  0.0,  0.0,
		   -0.5,  0.5, -0.5, -1.0,  0.0,  0.0,
		   -0.5, -0.5, -0.5, -1.0,  0.0,  0.0,
		   -0.5, -0.5,  0.5,  0.0,  0.0,  1.0, // front
			0.5, -0.5,  0.5,  0.0,  0.0,  1.0,
			0.5,  0.5,  0.5,  0.0,  0.0,  1.0,
		   -0.5,  0.5,  0.5,  0.0,  0.0,  1.0,
		   -0.5, -0.5, -0.5,  0.0,  0.0, -1.0, // back
			0.5, -0.5, -0.5,  0.0,  0.0, -1.0,
			0.5,  0.5, -0.5,  0.0,  0.0, -1.0,
		   -0.5,  0.5, -0.5,  0.0,  0.0, -1.0
		];

		Self {
			indices,
			attributes
		}
	}
}